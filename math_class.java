import java.util.*;
//import java.math.*;
public class math_class{
	public static void main(String[] args) {
		System.out.println(Math.abs(-26.7));   // it gives absoulute value like give positive value as answer whether it is positive or negative
		System.out.println(Math.ceil(26.7));   // it gives value without Decimal and above the value like here whatever between 26.01 to 26.9 the answer must be 27
		System.out.println(Math.floor(26.7));   // it gives value without Decimal and below the value like here whatever between 26.01 to 26.9 the answer must be 26
		System.out.println(Math.max(8.6,5.5));   // it will take two arguments and gives output as maximum of two
		System.out.println(Math.min(8.6,5.5));   // it will take two arguments and gives output as minimum of two
		System.out.println(Math.pow(2,9)); //it will take 2^9=gives answer 512
		System.out.println(Math.sqrt(9)); //it will finds Square root gives answer 3
	
	}
}